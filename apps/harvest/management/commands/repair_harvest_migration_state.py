    def handle(self, *args, **options):
        if connection.vendor != "postgresql":
            self.stdout.write("Harvest migration repair is only needed on PostgreSQL.")
            return

        recorder = MigrationRecorder(connection)
        with connection.cursor() as cursor:
            cursor.execute("SELECT to_regclass('public.harvest_raw_has_desc_lock_idx')")
            if cursor.fetchone()[0]:
                for name in self.BASELINE_MIGRATIONS:
                    recorder.record_applied("harvest", name)

            self._ensure_selective_schema(cursor)

        seed_module = import_module(
            "harvest.migrations.0054_harvestfiltersnapshot_harvestrolecategory_and_more"
        )
        seed_module.seed_selective_filter(apps, None)
        recorder.record_applied("harvest", self.SELECTIVE_MIGRATION)
        self.stdout.write(self.style.SUCCESS("Harvest migration state repaired."))

